class Napkin {
    public static void main(String[] args) {
        String brand = "Scott";
        System.out.println(brand);
        brand = "Tork";
        System.out.println(brand);

        int quantity = 5;
        System.out.println(quantity);
        quantity = 3;
        System.out.println(quantity);

        float price = 12.50f;
        System.out.println(price);
        price = 10.99f;
        System.out.println(price);

        double weight = 0.250;
        System.out.println(weight);
        weight = 0.200;
        System.out.println(weight);

        boolean isSoft = true;
        System.out.println(isSoft);
        isSoft = false;
        System.out.println(isSoft);

        char grade = 'A';
        System.out.println(grade);
        grade = 'B';
        System.out.println(grade);

        byte layers = 2;
        System.out.println(layers);
        layers = 3;
        System.out.println(layers);

        short stock = 100;
        System.out.println(stock);
        stock = 80;
        System.out.println(stock);

        long barcode = 987654321012L;
        System.out.println(barcode);
        barcode = 123456789101L;
        System.out.println(barcode);
    }
}

