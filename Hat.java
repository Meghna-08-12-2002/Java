class Hat {
    public static void main(String[] args) {
        String brand = "Puma";
        System.out.println(brand);
        brand = "Nike";
        System.out.println(brand);

        int quantity = 10;
        System.out.println(quantity);
        quantity = 22;
        System.out.println(quantity);

        float price = 1299.50f;
        System.out.println(price);
        price = 1499.99f;
        System.out.println(price);

        double weight = 0.500;
        System.out.println(weight);
        weight = 0.450;
        System.out.println(weight);

        boolean isAdjustable = true;
        System.out.println(isAdjustable);
        isAdjustable = false;
        System.out.println(isAdjustable);

        char rating = 'A';
        System.out.println(rating);
        rating = 'B';
        System.out.println(rating);

        byte size = 7;
        System.out.println(size);
        size = 8;
        System.out.println(size);

        short stock = 150;
        System.out.println(stock);
        stock = 120;
        System.out.println(stock);

        long barcode = 987654321012L;
        System.out.println(barcode);
        barcode = 123456789101L;
        System.out.println(barcode);
    }
}
