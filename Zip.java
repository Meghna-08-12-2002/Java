class Zip {
    String brand;
    String type;
    String color;
    double price;
    boolean isWaterproof;
    boolean isRustProof;
    int length;
    String material;
    boolean isDurable;
    String usage;
    int teethCount;
    float rating;
    String manufacturingCountry;
    boolean hasLockMechanism;
    boolean isWashable;

    public Zip(String brand, String type, String color, double price, boolean isWaterproof, boolean isRustProof,
               int length, String material, boolean isDurable, String usage, int teethCount,
               float rating, String manufacturingCountry, boolean hasLockMechanism, boolean isWashable) {
        this.brand = brand;
        this.type = type;
        this.color = color;
        this.price = price;
        this.isWaterproof = isWaterproof;
        this.isRustProof = isRustProof;
        this.length = length;
        this.material = material;
        this.isDurable = isDurable;
        this.usage = usage;
        this.teethCount = teethCount;
        this.rating = rating;
        this.manufacturingCountry = manufacturingCountry;
        this.hasLockMechanism = hasLockMechanism;
        this.isWashable = isWashable;
    }
}

