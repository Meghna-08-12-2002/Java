class Ring {
    public static void main(String[] args) {
        String brand = "Mejuri";
        System.out.println(brand);
        brand = "Zales";
        System.out.println(brand);

        int quantity = 10;
        System.out.println(quantity);
        quantity = 22;
        System.out.println(quantity);

        float price = 4999.50f;
        System.out.println(price);
        price = 5999.99f;
        System.out.println(price);

        double weight = 5.750;
        System.out.println(weight);
        weight = 6.250;
        System.out.println(weight);

        boolean isGold = true;
        System.out.println(isGold);
        isGold = false;
        System.out.println(isGold);

        char rating = 'A';
        System.out.println(rating);
        rating = 'B';
        System.out.println(rating);

        byte warranty = 3;
        System.out.println(warranty);
        warranty = 5;
        System.out.println(warranty);

        short stock = 50;
        System.out.println(stock);
        stock = 30;
        System.out.println(stock);

        long barcode = 987654321012L;
        System.out.println(barcode);
        barcode = 123456789101L;
        System.out.println(barcode);
    }
}
