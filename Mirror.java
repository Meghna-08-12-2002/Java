class Mirror {
    String color;
    String placeOfUse;
    int price;
    String shape;
    boolean fogFree;
    boolean waterResistance;
    String brand;
    char size;
    double height;
    double width;
    boolean lightWeight;
    String frameDesign;
    String typeOfGlass;
    float rating;
    String manufacturingState;
    boolean isScratchResistant;
    String typeOfReflection;
    String cleaningMethod;
    int warrantyPeriod;
    int discount;

    public Mirror(String color, String placeOfUse, int price, String shape, boolean fogFree, boolean waterResistance, 
                  String brand, char size, double height, double width, boolean lightWeight, String frameDesign, 
                  String typeOfGlass, float rating, String manufacturingState, boolean isScratchResistant, 
                  String typeOfReflection, String cleaningMethod, int warrantyPeriod, int discount) {
        this.color = color;
        this.placeOfUse = placeOfUse;
        this.price = price;
        this.shape = shape;
        this.fogFree = fogFree;
        this.waterResistance = waterResistance;
        this.brand = brand;
        this.size = size;
        this.height = height;
        this.width = width;
        this.lightWeight = lightWeight;
        this.frameDesign = frameDesign;
        this.typeOfGlass = typeOfGlass;
        this.rating = rating;
        this.manufacturingState = manufacturingState;
        this.isScratchResistant = isScratchResistant;
        this.typeOfReflection = typeOfReflection;
        this.cleaningMethod = cleaningMethod;
        this.warrantyPeriod = warrantyPeriod;
        this.discount = discount;
    }
}
